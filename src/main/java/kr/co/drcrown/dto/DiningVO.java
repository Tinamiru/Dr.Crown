package kr.co.drcrown.dto;

public class DiningVO
{
	private String dinCode;
	private String dinMenu;
	private String dinMealDate;
	private String dinWriter;
	
	public String getDinCode()
	{
		return dinCode;
	}
	public void setDinCode(String dinCode)
	{
		this.dinCode = dinCode;
	}
	public String getDinMenu()
	{
		return dinMenu;
	}
	public void setDinMenu(String dinMenu)
	{
		this.dinMenu = dinMenu;
	}
	public String getDinMealDate()
	{
		return dinMealDate;
	}
	public void setDinMealDate(String dinMealDate)
	{
		this.dinMealDate = dinMealDate;
	}
	public String getDinWriter()
	{
		return dinWriter;
	}
	public void setDinWriter(String dinWriter)
	{
		this.dinWriter = dinWriter;
	}
	
	
}
