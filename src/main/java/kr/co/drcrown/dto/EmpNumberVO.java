package kr.co.drcrown.dto;

public class EmpNumberVO
{
	private String empId;			// 사원번호
	private String empName;		// 이름
	private String empPhone;		// 전화번호
	private String empPos;			// 직책
	private String empAuthority;	// 권한
	private String empEmpnum;		// 사번
	
	public String getEmpId()
	{
		return empId;
	}
	public void setEmpId(String empId)
	{
		this.empId = empId;
	}
	public String getEmpName()
	{
		return empName;
	}
	public void setEmpName(String empName)
	{
		this.empName = empName;
	}
	public String getEmpPhone()
	{
		return empPhone;
	}
	public void setEmpPhone(String empPhone)
	{
		this.empPhone = empPhone;
	}
	public String getEmpPos()
	{
		return empPos;
	}
	public void setEmpPos(String empPos)
	{
		this.empPos = empPos;
	}
	public String getEmpAuthority()
	{
		return empAuthority;
	}
	public void setEmpAuthority(String empAuthority)
	{
		this.empAuthority = empAuthority;
	}
	public String getEmpEmpnum()
	{
		return empEmpnum;
	}
	public void setEmpEmpnum(String empEmpnum)
	{
		this.empEmpnum = empEmpnum;
	}
	
	
	
}
