package kr.co.drcrown.dto;

public class EquipmentVO
{
	private String equCode;
	private String equName;
	private String equAmount;
	private String equMaker;
	
	public String getEquCode()
	{
		return equCode;
	}
	public void setEquCode(String equCode)
	{
		this.equCode = equCode;
	}
	public String getEquName()
	{
		return equName;
	}
	public void setEquName(String equName)
	{
		this.equName = equName;
	}
	public String getEquAmount()
	{
		return equAmount;
	}
	public void setEquAmount(String equAmount)
	{
		this.equAmount = equAmount;
	}
	public String getEquMaker()
	{
		return equMaker;
	}
	public void setEquMaker(String equMaker)
	{
		this.equMaker = equMaker;
	}
	
	
}
