package kr.co.drcrown.dto;


public class FileTbVO {
	
	private String fileCode;          	  // 파일코드
	private String noticeNo;    		  // 공지번호
	private String drugNo;			      // 약번호
	private String minfoCode;			  // 의학게시글코드
	private String empId;			      // 사원번호
	private String recNo;		    	  // 진료내용코드
	private String mCode;	    		  // 메뉴번호
	private String docCode;			  // 문서서식코드
	
	
	public String getFileCode() {
		return fileCode;
	}
	public void setFileCode(String fileCode) {
		this.fileCode = fileCode;
	}
	public String getNoticeNo() {
		return noticeNo;
	}
	public void setNoticeNo(String noticeNo) {
		this.noticeNo = noticeNo;
	}
	public String getDrugNo() {
		return drugNo;
	}
	public void setDrugNo(String drugNo) {
		this.drugNo = drugNo;
	}
	public String getMinfoCode() {
		return minfoCode;
	}
	public void setMinfoCode(String minfoCode) {
		this.minfoCode = minfoCode;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getRecNo() {
		return recNo;
	}
	public void setRecNo(String recNo) {
		this.recNo = recNo;
	}
	public String getmCode() {
		return mCode;
	}
	public void setmCode(String mCode) {
		this.mCode = mCode;
	}
	public String getDocCode() {
		return docCode;
	}
	public void setDocCode(String docCode) {
		this.docCode = docCode;
	}
	
	  					
}
	