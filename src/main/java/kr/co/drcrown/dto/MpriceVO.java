package kr.co.drcrown.dto;

public class MpriceVO {
	
	private String mprCode;         	 // 단가코드
	private String mprNo; 			     // 분류번호
	private String mprKo;    			 // 한글명
	private String mprEn;    			 // 영문명
	private String mprPer;    			 // 본인부담률
	private String mprClass; 			 // 세분류
	private String mprPrice;    		 // 의료행위단가
	
	
	public String getMprCode() {
		return mprCode;
	}
	public void setMprCode(String mprCode) {
		this.mprCode = mprCode;
	}
	public String getMprNo() {
		return mprNo;
	}
	public void setMprNo(String mprNo) {
		this.mprNo = mprNo;
	}
	public String getMprKo() {
		return mprKo;
	}
	public void setMprKo(String mprKo) {
		this.mprKo = mprKo;
	}
	public String getMprEn() {
		return mprEn;
	}
	public void setMprEn(String mprEn) {
		this.mprEn = mprEn;
	}
	public String getMprPer() {
		return mprPer;
	}
	public void setMprPer(String mprPer) {
		this.mprPer = mprPer;
	}
	public String getMprClass() {
		return mprClass;
	}
	public void setMprClass(String mprClass) {
		this.mprClass = mprClass;
	}
	public String getMprPrice() {
		return mprPrice;
	}
	public void setMprPrice(String mprPrice) {
		this.mprPrice = mprPrice;
	}
	
	
}
	