package kr.co.drcrown.dto;

public class PreDrugVO {
	
	private String prdCode;         // 초방약코드
	private String prdNo;			// 약번호
	private String prsCode;			// 처방코드
	private String prdName;			// 품목명
	private String prdMclass;		// 의약품 구분
	private String prdDclass;		// 마약류 구분
	private String prdAmount;		// 수량
	
	
	public String getPrdCode() {
		return prdCode;
	}
	public void setPrdCode(String prdCode) {
		this.prdCode = prdCode;
	}
	public String getPrdNo() {
		return prdNo;
	}
	public void setPrdNo(String prdNo) {
		this.prdNo = prdNo;
	}
	public String getPrsCode() {
		return prsCode;
	}
	public void setPrsCode(String prsCode) {
		this.prsCode = prsCode;
	}
	public String getPrdName() {
		return prdName;
	}
	public void setPrdName(String prdName) {
		this.prdName = prdName;
	}
	public String getPrdMclass() {
		return prdMclass;
	}
	public void setPrdMclass(String prdMclass) {
		this.prdMclass = prdMclass;
	}
	public String getPrdDclass() {
		return prdDclass;
	}
	public void setPrdDclass(String prdDclass) {
		this.prdDclass = prdDclass;
	}
	public String getPrdAmount() {
		return prdAmount;
	}
	public void setPrdAmount(String prdAmount) {
		this.prdAmount = prdAmount;
	}
	
	
}
	