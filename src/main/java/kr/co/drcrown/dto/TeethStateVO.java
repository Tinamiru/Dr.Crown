package kr.co.drcrown.dto;


public class TeethStateVO {
	
	private String teeCode;         // 치아상태코드
	private String teeNum;          // 치아번호
	private String oralCode;    	// 치아진단코드
	private String teeState;    	// 치아상태
	
	public String getTeeCode() {
		return teeCode;
	}
	public void setTeeCode(String teeCode) {
		this.teeCode = teeCode;
	}
	public String getTeeNum() {
		return teeNum;
	}
	public void setTeeNum(String teeNum) {
		this.teeNum = teeNum;
	}
	public String getOralCode() {
		return oralCode;
	}
	public void setOralCode(String oralCode) {
		this.oralCode = oralCode;
	}
	public String getTeeState() {
		return teeState;
	}
	public void setTeeState(String teeState) {
		this.teeState = teeState;
	}
	
	
	
}
	